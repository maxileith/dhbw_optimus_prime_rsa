package optimus.prime.rsa.communication;

public class Solution {
    private int prime1 = 0;
    private int prime2 = 0;

    public Solution(int prime1, int prime2) {
        this.prime1 = prime1;
        this.prime2 = prime2;
    }

    public int getPrime1() {
        return prime1;
    }

    public int getPrime2() {
        return prime2;
    }
}
